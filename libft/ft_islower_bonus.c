int	ft_islower(int c)
{
	int	re;

	if (c == 0)
	{
		return (0);
	}
	if (((((c) >= 'a') && (c) <= 'z')))
	{
		re = 1;
	}
	else
	{
		re = 0;
	}
	return (re);
}
