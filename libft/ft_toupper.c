int	ft_toupper(int c)
{
	int	re;

	if (c < -1)
		return ((unsigned char)c);
	if (c >= 'a' && c <= 'z')
	{
		re = c - 32;
	}
	else
	{
		re = c;
	}
	return (re);
}
