int	ft_islower(int c)
{
	int re;

	re = 1;
	if (c == 0)
	{
		return (0);
	}
	if (((((c) >= 'a') && (c) <= 'z')))
	{
		;
	}
	else
	{
		re = 0;
	}
	return (re);
}
